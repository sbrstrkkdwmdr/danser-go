package math87

func Add87(a, b float32) float32 {
	return float32(float64(a) + float64(b))
}

func Sub87(a, b float32) float32 {
	return float32(float64(a) - float64(b))
}

func Mul87(a, b float32) float32 {
	return float32(float64(a) * float64(b))
}

func Div87(a, b float32) float32 {
	return float32(float64(a) / float64(b))
}
